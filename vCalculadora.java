    private void btnModificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnModificarMouseClicked
                                               
      try {
            // Verificar que los campos no estén vacíos
            if (txtID.getText().isEmpty() || txtMonto.getText().isEmpty() || txtFecha.getText().isEmpty() || txtDesc.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos obligatorios.");
                return;
            }

            int id = Integer.parseInt(txtID.getText());
            String tipo = txtTipo.getSelectedItem().toString();
            String descripcion = txtDesc.getText();
            double monto = Double.parseDouble(txtMonto.getText());
            LocalDate fecha = LocalDate.parse(txtFecha.getText());

            // Crear objeto Calculadora con los datos del formulario
            Calculadora cal = new Calculadora();
            cal.setId(id);
            cal.setTipo(tipo);
            cal.setDescripcion(descripcion);
            cal.setMonto(monto);
            cal.setFecha(fecha);

            // Llamar al método modificar del DAO
           if (dao.modificar(cal)) {
                JOptionPane.showMessageDialog(this, "Modificado con éxito");
                this.CargarTabla();
                this.CargarTotal();
                limpiarCampos(); // Limpiar los campos después de modificar
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo modificar");
            }
        } catch (Exception e) {
            
        }

    }//GEN-LAST:event_btnModificarMouseClicked

