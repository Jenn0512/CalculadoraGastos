    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
  int selectedRow = tblDatos.getSelectedRow();
        if (selectedRow != -1) {
            int id = (int) tblDatos.getValueAt(selectedRow, 0); // Obtener el id de la fila seleccionada
            Calculadora calculadora = new Calculadora(); // Crear una instancia de Calculadora (o tu objeto correspondiente)
            calculadora.setId(id); // Establecer el id en el objeto Calculadora

            // Llamar al método eliminar del DAO
            if (dao.eliminar(calculadora)) {
                JOptionPane.showMessageDialog(this, "Registro eliminado correctamente");
                CargarTabla(); // Método para actualizar la tabla después de eliminar
                CargarTotal(); // Método para actualizar el total o cualquier otro componente
                limpiarCampos(); // Método opcional para limpiar campos después de eliminar
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar el registro");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un registro para eliminar");
    }
    }//GEN-LAST:event_btnEliminarMouseClicked

