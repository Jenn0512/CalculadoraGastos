    private void btnGraficoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGraficoMouseClicked
       // 1. Obtener datos de los JTextPane
    String mesSeleccionado = jTextPaneMes.getText().toLowerCase().trim(); // Obtener texto del JTextPane y convertir a minúsculas
    int mes = obtenerNumeroMes(mesSeleccionado); // Convertir el nombre del mes a número

    String anioSeleccionado = jTextPaneAnio.getText().trim(); // Obtener texto del JTextPane
    int anio;
    try {
        anio = Integer.parseInt(anioSeleccionado); // Convertir a entero
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Ingrese un año válido (número entero).");
        return; // Salir del método si el año no es válido
    }

    // 2. Validar entrada
    if (mes == -1) {
        JOptionPane.showMessageDialog(this, "Ingrese un mes válido.");
        return;
    }

    // 3. Obtener datos del DAO
    DAOCalculadora dao = new DAOCalculadora();
    Map<String, Double> datosGrafico = dao.obtenerDatosParaGrafico(mes, anio);

    // 4. Crear dataset
    DefaultCategoryDataset dataset = new DefaultCategoryDataset();
    for (Map.Entry<String, Double> entry : datosGrafico.entrySet()) {
        String tipo = entry.getKey();
        Double total = entry.getValue();

        // Formatear el tipo para que se vea mejor en el gráfico
        String tipoFormateado = tipo.substring(0, 1).toUpperCase() + tipo.substring(1).toLowerCase();
        dataset.setValue(total, "Total", tipoFormateado); // (valor, serie, categoría)
    }

    // 5. Crear gráfico
    JFreeChart chart = ChartFactory.createBarChart(
            "Gastos e Ingresos por Mes", "Tipo", "Total", dataset,
            PlotOrientation.VERTICAL, true, true, false);

    // 6. Personalizar el gráfico (opcional)
    CategoryPlot plot = (CategoryPlot) chart.getPlot();
    NumberAxis rangeAxis = (NumberAxis) plot.getRangeAxis();
    rangeAxis.setRange(-500, 1700); // Formatear los números del eje Y


    // 7. Mostrar gráfico
    ChartFrame frame = new ChartFrame("Gráfico", chart);
    frame.pack();
    frame.setVisible(true);
        
// TODO add your handling code here:
    }//GEN-LAST:event_btnGraficoMouseClicked

